// Function Declaration
function greet() {
    console.log("Hello from a function declaration!");
}

// Function Expression
const sayGoodbye = function () {
    console.log("Goodbye from a function expression!");
};

// Arrow Function
const sayThanks = () => {
    console.log("Thanks from an arrow function!");
};

// Function Calls
greet();
sayGoodbye();
sayThanks();